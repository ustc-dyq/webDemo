CREATE TABLE `train_ticket` (
  `id` int(10) NOT NULL,
  `train_id` int(10) NOT NULL COMMENT '车次ID',
  `train_num` varchar(32) NOT NULL COMMENT '车次号',
  `train_type` tinyint(4) NOT NULL DEFAULT '0' COMMENT '车次类型：type：0:高铁,1:城际,2:动车,3:直达,4:特快,5:普快,6:其他',
  `train_type_name` varchar(16) NOT NULL DEFAULT '0' COMMENT '车次类型名称',
  `seat_type` tinyint(4) NOT NULL COMMENT '坐等：0：商务座,1：特等座,2：一等座,3：二等座,4：高级软卧,5：软卧,6：硬卧,7：软座,8：硬座,9：无座',
  `depart_city_code` varchar(8) NOT NULL COMMENT '出发城市ID',
  `dest_city_code` varchar(8) NOT NULL COMMENT '到达城市ID',
  `depart_city_name` varchar(16) NOT NULL COMMENT '出发城市',
  `dest_city_name` varchar(16) NOT NULL COMMENT '到达城市',
  `depart_station_code` varchar(8) NOT NULL COMMENT '出发站ID',
  `dest_station_code` varchar(8) NOT NULL COMMENT '到达站ID',
  `depart_station_name` varchar(16) NOT NULL COMMENT '出发站',
  `dest_station_name` varchar(16) NOT NULL COMMENT '到达站',
  `depart_arrive_time` varchar(8) NOT NULL COMMENT '出发站到站时间',
  `depart_depart_time` varchar(8) NOT NULL COMMENT '出发站出发时间',
  `dest_arrive_time` varchar(8) NOT NULL COMMENT '到达站到站时间',
  `dest_depart_time` varchar(8) NOT NULL COMMENT '到达站出发时间',
  `duration` int(11) NOT NULL DEFAULT '0' COMMENT '历时',
  `duration_day` tinyint(4) NOT NULL DEFAULT '1' COMMENT '历时',
  `depart_station_type` tinyint(4) NOT NULL DEFAULT '1' COMMENT '过路类型：0:始发；1:过路；2:终点',
  `dest_station_type` tinyint(4) NOT NULL DEFAULT '1' COMMENT '过路类型：0:始发；1:过路；2:终点',
  `price` decimal(10,2) DEFAULT NULL COMMENT '价格',
  `op_time` datetime NOT NULL COMMENT '操作时间',
  `del_flag` tinyint(4) DEFAULT '0' COMMENT '删除标识:0未删除,1已删除',
  PRIMARY KEY (`id`),
  UNIQUE KEY `uk_train_depart_dest` (`train_id`,`depart_station_code`,`dest_station_code`,`seat_type`),
  KEY `idx_depart_dest` (`depart_station_code`,`dest_station_code`),
  KEY `city_code_index` (`depart_city_code`,`dest_city_code`) USING BTREE
) ENGINE=InnoDB DEFAULT CHARSET=utf8;